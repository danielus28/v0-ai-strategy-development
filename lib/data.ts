// Map layer types
export type LayerType = "iso" | "egdi" | "gci" | "gari" | "ilia" | "oecd"

export interface CountryData {
  iso3: string
  country: string
  sc42_status?: string
  sc27_status?: string
  egdi_2024?: number
  gci_2024?: number
  gari_score?: number
  gari_rank?: number
  ilia_governance_score?: number
  total_policies?: number
  has_strategy?: boolean
  strategy_name?: string
  strategy_year?: number
}

// Combine all data for a unified country view
export function getCountryData(iso3: string): CountryData | null {
  const iso = isoMembershipData.find((d) => d.iso3 === iso3)
  const egdi = egdiData.find((d) => d.iso3 === iso3)
  const gci = gciData.find((d) => d.iso3 === iso3)
  const gari = gariData.find((d) => d.iso3 === iso3)
  const ilia = iliaData.find((d) => d.iso3 === iso3)
  const oecd = oecdPoliciesData.find((d) => d.iso3 === iso3)
  const strategy = aiStrategiesData.find((d) => d.iso3 === iso3)

  if (!iso && !egdi && !gci && !gari) return null

  return {
    iso3,
    country: iso?.country || egdi?.country || gci?.country || gari?.country || "",
    sc42_status: iso?.sc42_status,
    sc27_status: iso?.sc27_status,
    egdi_2024: egdi?.egdi_2024,
    gci_2024: gci?.gci_2024,
    gari_score: gari?.gari_score ?? undefined,
    gari_rank: gari?.gari_rank ?? undefined,
    ilia_governance_score: ilia?.ilia_governance_score,
    total_policies: oecd?.total_policies,
    has_strategy: !!strategy,
    strategy_name: strategy?.strategy_name,
    strategy_year: strategy?.start_date,
  }
}

// Get all unique countries
export function getAllCountries(): string[] {
  const countries = new Set<string>()
  isoMembershipData.forEach((d) => countries.add(d.iso3))
  egdiData.forEach((d) => countries.add(d.iso3))
  gciData.forEach((d) => countries.add(d.iso3))
  gariData.forEach((d) => countries.add(d.iso3))
  return Array.from(countries)
}
